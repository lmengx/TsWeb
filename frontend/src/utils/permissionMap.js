export const getPermissionName = (permission) => {
  return permissionMap[permission] || permission
}

export const searchPermissions = (query) => {
  if (!query || !query.trim()) return []
  
  const q = query.toLowerCase().trim()
  const hasChinese = /[\u4e00-\u9fa5]/.test(query)
  
  let results = []
  
  if (hasChinese) {
    results = Object.entries(permissionMap)
      .filter(([key, value]) => value.includes(query))
      .map(([key, value]) => ({ key, value }))
      .slice(0, 6)
  } else {
    const englishMatches = Object.entries(permissionMap)
      .filter(([key]) => key.toLowerCase().includes(q))
      .map(([key, value]) => ({ key, value }))
      .slice(0, 3)
    
    const chineseMatches = Object.entries(permissionMap)
      .filter(([key, value]) => value.includes(query))
      .map(([key, value]) => ({ key, value }))
      .slice(0, 3)
    
    const seen = new Set()
    englishMatches.forEach(item => {
      if (!seen.has(item.key)) {
        seen.add(item.key)
        results.push(item)
      }
    })
    chineseMatches.forEach(item => {
      if (!seen.has(item.key)) {
        seen.add(item.key)
        results.push(item)
      }
    })
    
    results = results.slice(0, 6)
  }
  
  return results
}
